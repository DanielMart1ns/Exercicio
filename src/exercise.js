"use strict";
//Multiplication function
const multiplicationNumbers = (firstData, secondData) => {
    return firstData * secondData;
};
const firstNumber = 100;
const secondNumber = 5;
console.log(multiplicationNumbers(firstNumber, secondNumber));
//Hello function
const sayHello = (nameEntered) => {
    return `Hello ${nameEntered}`;
};
const userNameEntered = 'Ana';
console.log(sayHello(userNameEntered));
